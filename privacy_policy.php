      const observer = new IntersectionObserver(entries => {
        entries.forEach(entry => {
          entry.target.classList.forEach(namakelas => {
            if(namakelas == "munculdown") {
              if (entry.isIntersecting) {
                entry.target.classList.add('munculdown_animation');
                return;
              }
              entry.target.classList.remove('munculdown_animation');
            } else if(namakelas == "munculright" || namakelas == "munculright2") {
              if (entry.isIntersecting) {
                entry.target.classList.add('munculright_animation');
                return;
              }
              entry.target.classList.remove('munculright_animation');
            } else if(namakelas == "munculleft" || namakelas == "munculleft2" || namakelas == "munculleft3") {
              if (entry.isIntersecting) {
                entry.target.classList.add('munculleft_animation');
                return;
              }
              entry.target.classList.remove('munculleft_animation');
            } else if(namakelas == "munculscale") {
              if (entry.isIntersecting) {
                entry.target.classList.add('munculscale_animation');
                return;
              }
              entry.target.classList.remove('munculscale_animation');
            }
          });    
        });
      });
      observer.observe(document.querySelector('.munculdown'));
